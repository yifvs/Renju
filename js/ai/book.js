    // 添加一个变例到棋谱库
    _addVariation: function(moves, priority) {
        // 构建局面签名
        let signature = "";
        for (let i = 0; i < moves.length - 1; i++) {
            if (i > 0) signature += ",";
            signature += moves[i][0] + "_" + moves[i][1];
        }
        
        // 推荐的应着（最后一手）
        const lastMove = moves[moves.length - 1];
        
        // 只存对手下完后的局面，AI的应着
        // 签名长度为奇数表示轮到黑（AI执黑时匹配），偶数表示轮到白（AI执白时匹配）
        // 我们需要同时存两种视角
        
        // 对于已有签名，如果优先级更高则覆盖
        if (!this.book[signature] || this.book[signature].priority > priority) {
            this.book[signature] = {
                row: lastMove[0],
                col: lastMove[1],
                priority: priority
            };
        }
    },

    // 查找当前局面是否匹配棋谱
    // 返回匹配的推荐落子位置，或null
    lookup: function(moveHistory) {
        if (moveHistory.length === 0) return null;
        
        // 从最长的可能匹配开始找
        for (let len = moveHistory.length; len >= 2; len -= 2) {
            // 只考虑完整回合（偶数的走法序列，因为对手走完轮到AI）
            if (len % 2 !== 0) len--; 
            if (len < 2) break;
            
            const history = moveHistory.slice(0, len);
            const signature = history.map(m => m.row + "_" + m.col).join(",");
            
            if (this.book[signature]) {
                return this.book[signature];
            }
        }
        
        // 也尝试匹配奇数长度的（某些开局库条目可能以黑方视角存储）
        for (let len = moveHistory.length - 1; len >= 1; len -= 2) {
            if (len < 1) break;
            
            const history = moveHistory.slice(0, len);
            const signature = history.map(m => m.row + "_" + m.col).join(",");
            
            if (this.book[signature]) {
                return this.book[signature];
            }
        }
        
        return null;
    },

    // 获取开局库大小
    size: function() {
        return Object.keys(this.book).length;
    }
